/// Compact table builder used by every generated data file.
/// Turns a slice of (name, l, u, r, low) tuples into a ChunkMap.
pub(crate) fn chunks(entries: &[(&str, f32, f32, f32, f32)]) -> ChunkMap {
    entries
        .iter()
        .map(|(name, l, u, r, low)| (name.to_string(), ChunkCoords(*l, *u, *r, *low)))
        .collect()
}

