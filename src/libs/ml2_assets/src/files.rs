pub fn get_filepath_registry<T: Spel2ChaCha>(chacha: &T) -> HashMap<Vec<u8>, &'static [u8]> {
    let mut registry = HashMap::new();
    for file in KNOWN_FILEPATHS {
        let hash = chacha.hash_filepath(file);
        registry.insert(hash, file);
    }
    registry
}
